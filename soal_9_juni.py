class TrieNode:

    def __init__(self):
        self.children = {}
        self.words = []


class Trie:

    def __init__(self):
        self.root = TrieNode()

    def insert(self, word):

        node = self.root

        for char in word.lower():

            if char not in node.children:
                node.children[char] = TrieNode()

            node = node.children[char]
            node.words.append(word)

    def autocomplete(self, prefix):

        node = self.root

        for char in prefix.lower():

            if char not in node.children:
                return []

            node = node.children[char]

        return node.words


