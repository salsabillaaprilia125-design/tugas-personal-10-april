class AVLNode:

    def __init__(self, document):

        self.doc = document
        self.left = None
        self.right = None
        self.height = 1


class AVLTree:

    def height(self, node):
        return node.height if node else 0

    def balance(self, node):

        if not node:
            return 0

        return (
            self.height(node.left)
            - self.height(node.right)
        )

    def right_rotate(self, y):

        x = y.left
        t2 = x.right

        x.right = y
        y.left = t2

        y.height = max(
            self.height(y.left),
            self.height(y.right)
        ) + 1

        x.height = max(
            self.height(x.left),
            self.height(x.right)
        ) + 1

        return x

    def left_rotate(self, x):

        y = x.right
        t2 = y.left

        y.left = x
        x.right = t2

        x.height = max(
            self.height(x.left),
            self.height(x.right)
        ) + 1

        y.height = max(
            self.height(y.left),
            self.height(y.right)
        ) + 1

        return y

    def insert(self, root, document):

        if root is None:
            return AVLNode(document)

        if document.id < root.doc.id:
            root.left = self.insert(root.left, document)

        elif document.id > root.doc.id:
            root.right = self.insert(root.right, document)

        else:
            return root

        root.height = max(
            self.height(root.left),
            self.height(root.right)
        ) + 1

        bf = self.balance(root)

        if bf > 1 and document.id < root.left.doc.id:
            return self.right_rotate(root)

        if bf < -1 and document.id > root.right.doc.id:
            return self.left_rotate(root)

        if bf > 1 and document.id > root.left.doc.id:
            root.left = self.left_rotate(root.left)
            return self.right_rotate(root)

        if bf < -1 and document.id < root.right.doc.id:
            root.right = self.right_rotate(root.right)
            return self.left_rotate(root)

        return root

    def search(self, root, doc_id):

        if root is None:
            return None

        if root.doc.id == doc_id:
            return root.doc

        if doc_id < root.doc.id:
            return self.search(root.left, doc_id)

        return self.search(root.right, doc_id)

    def inorder(self, root):

        if root:

            self.inorder(root.left)

            print(
                root.doc.id,
                root.doc.title,
                root.doc.score
            )

            self.inorder(root.right)


